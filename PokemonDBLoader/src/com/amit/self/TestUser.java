package com.amit.self;

public class TestUser {

	private String user;
	private String host;

	public TestUser(String user, String host) {
		super();
		this.user = user;
		this.host = host;
	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public String getHost() {
		return host;
	}

	public void setHost(String host) {
		this.host = host;
	}

}
